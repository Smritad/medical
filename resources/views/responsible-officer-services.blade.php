                                        <p>A Responsible Officer will:</p>
                                        <ul class="custom-list">
                                            <li>Manage revalidation and appraisals for contracted or temporary doctors</li>
                                            <li>Ensure all doctors working under your agency remain fit to practise</li>
                                            <li>Help maintain your CQC registration and reputation</li>
                                            <li>Provide clear oversight on any clinical or conduct concerns</li>
