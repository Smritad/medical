                                <div class="panel-heading" role="tab" id="heading4faq">
                                    <h4 class="panel-title">
                                        <a class="collapsed" role="button" data-toggle="collapse" data-parent="#faqAccordion" href="#collapse4faq" aria-expanded="false" aria-controls="collapse4faq">
                                            4. Is it mandatory to appoint a GMC Responsible Officer for revalidation?
                                        </a>
                                    </h4>
                                </div>
                                <div id="collapse4faq" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading4faq">
                                    <div class="panel-body">
                                        <p>Yes. If your organisation is classified as a designated body, you are legally obligated to appoint a Responsible Officer to handle revalidation.</p>
                                        <p>Here's why this matters:</p>
                                        <ul class="custom-list">
                                            <li>Only a GMC Responsible Officer can submit valid revalidation recommendations</li>
                                            <li>Without an RO, your doctors may become non-compliant, affecting their license to practice</li>
                                            <li>NHS England and the Care Quality Commission (CQC) enforce these requirements strictly</li>
                                        </ul>
                                        <p>By appointing an RO, you demonstrate your commitment to safe and regulated medical practice.</p>
                                    </div>
                                </div>
                            </div>

