                                        <a class="collapsed" role="button" data-toggle="collapse" data-parent="#faqAccordion" href="#collapse3faq" aria-expanded="false" aria-controls="collapse3faq">
                                            3. What does a Responsible Officer do in a designated body?
                                        </a>
                                    </h4>
                                </div>
                                <div id="collapse3faq" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading3faq">
                                    <div class="panel-body">
                                        <p>A GMC Responsible Officer plays a vital role in maintaining high medical and ethical standards within your organisation. Key responsibilities include:</p>
                                        <ul class="custom-list">
                                            <li>Ensuring all doctors undergo annual GMC-compliant appraisals</li>
                                            <li>Making formal revalidation recommendations to the GMC every five years</li>
                                            <li>Monitoring and managing fitness-to-practice concerns</li>
                                            <li>Overseeing and improving clinical governance systems</li>
                                            <li>Supporting a culture of accountability and patient safety</li>
                                        </ul>
                                        <p>Their role directly impacts your organisation's ability to maintain CQC registration and avoid regulatory penalties.</p>
                                    </div>
                                </div>
                            </div>

                            <!-- FAQ 4 -->
