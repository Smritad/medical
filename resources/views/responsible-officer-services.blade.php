                                            7. Do locum agencies need a Responsible Officer for contracted doctors?
                                        </a>
                                    </h4>
                                </div>
                                <div id="collapse7faq" class="panel-collapse collapse" role="tabpanel" aria-labelledby="heading7faq">
                                    <div class="panel-body">
