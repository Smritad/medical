                var group = document.getElementById('faqAccordion');
                if (!group) return;

                var triggers = group.querySelectorAll('[data-toggle="collapse"]');

                function closePanel(panel, trigger) {
                    panel.classList.remove('is-open', 'in');
                    if (trigger) {
                        trigger.setAttribute('aria-expanded', 'false');
                        trigger.classList.add('collapsed');
                    }
                }

                function openPanel(panel, trigger) {
                    panel.classList.add('is-open', 'in');
                    if (trigger) {
                        trigger.setAttribute('aria-expanded', 'true');
                        trigger.classList.remove('collapsed');
                    }
                }

                // Initialise: any panel already marked "in" starts open.
                group.querySelectorAll('.panel-collapse').forEach(function (panel) {
                    var trigger = group.querySelector('[href="#' + panel.id + '"]');
                    if (panel.classList.contains('in')) {
                        openPanel(panel, trigger);
                    } else {
                        closePanel(panel, trigger);
                    }
                });

                triggers.forEach(function (trigger) {
                    trigger.addEventListener('click', function (e) {
                        e.preventDefault();

                        var targetSel = trigger.getAttribute('href') ||
                                        trigger.getAttribute('data-target');
                        if (!targetSel) return;

                        var panel = group.querySelector(targetSel);
                        if (!panel) return;

                        var isOpen = panel.classList.contains('is-open');

                        // Single-open behaviour (data-parent) — close the rest.
                        group.querySelectorAll('.panel-collapse').forEach(function (p) {
                            if (p !== panel) {
                                closePanel(p, group.querySelector('[href="#' + p.id + '"]'));
                            }
                        });

                        if (isOpen) {
                            closePanel(panel, trigger);
                        } else {
                            openPanel(panel, trigger);
                        }
                    });
                });
            });
        })();
    </script>
